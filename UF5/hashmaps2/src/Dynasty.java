public class Dynasty {
  int numKings;
  String[] kings;
  int numSuccesors;
  String [] sucessors;

  public Dynasty(int nKings, String[] kings, int nSucessors, String[] sucessors) {
    this.numKings = nKings;
    this.kings = kings;
    this.numSuccesors = nSucessors;
    this.sucessors = sucessors;
  }
}
